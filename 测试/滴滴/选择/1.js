// 以下关于解构赋值说法正确的是
// A.解构赋值不能用于嵌套的对象或数组
// B.解构赋值只适用于固定长度的数组
// C.解构赋值可以方便地交换两个变量的值
// D.解构赋值只能用于声明变量，不能用于赋值操作

const obj = {
    a: 1,
    b: { c: 2 }
};
const { b: { c } } = obj; // 嵌套对象的解构赋值，能成功获取 c = 2
console.log(c); // 2

const arr = [1, 2, 3, 4];
const [a, b] = arr; // 只需前两个元素，数组长度不影响
console.log(a, b); // 1 2

let x = 1, y = 2;
[x, y] = [y, x]; // 执行后 x = 2，y = 1，成功交换

// let a, b;
({ a, b } = { a: 1, b: 2 }); // 先声明变量，再通过解构赋值给变量赋值
console.log(a, b); // 1 2